public class Datos {
    
    private long dpi;
    private int llave;

    public Datos(long dpi, int llave) {
        this.dpi = dpi;
        this.llave = llave;
    }

    public long getDpi() {
        return dpi;
    }

    public void setDpi(long dpi) {
        this.dpi = dpi;
    }

    public int getLlave() {
        return llave;
    }

    public void setLlave(int llave) {
        this.llave = llave;
    }
    
    
}
